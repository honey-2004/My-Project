/** @var AttributeRepositoryInterface $attributeRepository */
$attributeRepository = $objectManager->get(AttributeRepositoryInterface::class);

// Load the attribute
$attribute = $attributeRepository->get('catalog_product', $attributeCode);

// Retrieve existing options
$existingOptions = $attribute->getOptions();
$existingOptionLabels = [];

// Build existing options array to avoid duplicates
foreach ($existingOptions as $option) {
    $existingOptionLabels[$option->getLabel()] = $option->getValue();
}

// Prepare new options to add only if they don't already exist
$newOptionData = [];
$optionFactory = $objectManager->get(OptionFactory::class);
$addedOptions = false;

foreach ($newOptions as $index => $label) {
    if (!isset($existingOptionLabels[$label])) {
        // Create new option
        $option = $optionFactory->create();
        $option->setLabel($label);
        $option->setSortOrder(count($existingOptions) + $index);

        $newOptionData['option_' . (count($existingOptionLabels) + $index)] = [$label];
        $addedOptions = true;
    }
}

// If there are new options to add, proceed with updating the attribute
if ($addedOptions) {
    $attribute->setData(
        'option',
        ['value' => $newOptionData]
    );

    // Save the attribute with new options
    try {
        $attributeRepository->save($attribute);
        echo "New options added to attribute '{$attributeCode}': " . implode(', ', $newOptions) . "\n";
    } catch (\Exception $e) {
        echo 'Error saving attribute: ' . $e->getMessage() . "\n";
    }
} else {
    echo "No new options to add. All options already exist for attribute '{$attributeCode}'.\n";
}
